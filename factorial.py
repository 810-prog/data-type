# 4. Write a while loop to calculate the factorial of a given number.
num = 1 
mul = 1
while num <=5:
    mul = mul * num 
    num +=1
    print("the factorial given number",mul)


# output 
# the factorial given number 1
# the factorial given number 2
# the factorial given number 6
# the factorial given number 24
# the factorial given number 120
